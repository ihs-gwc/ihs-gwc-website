function includeHTML() {
  var z, i, elmnt, file, xhttp;
  //loop through a collection of all HTML elements:
  z = document.getElementsByTagName("*");
  for (i = 0; i < z.length; i++) {
    elmnt = z[i];

    //menu only:
    function activateNavItem() {
      //get the attribute active-item of the element:
      activeItem = elmnt.getAttribute("active-item");
      if (activeItem) {
        //find the correct nav item and add the active class to it
        document.getElementById(activeItem).classList.add('active');
      }
    };

    //all pages:
    //get the attribute w3-include-html of the element:
    file = elmnt.getAttribute("w3-include-html");
    if (file) {
      //make an HTTP request using the attribute value as the file name:
      xhttp = new XMLHttpRequest();
      xhttp.onreadystatechange = function() {
        if (this.readyState == 4) {
          if (this.status == 200) {elmnt.innerHTML = this.responseText;}
          if (this.status == 404) {elmnt.innerHTML = "Page not found.";}
          //remove the attribute, and call this function once more:
          elmnt.removeAttribute("w3-include-html");
          includeHTML();
          activateNavItem();
        }
      }
      xhttp.open("GET", file, true);
      xhttp.send();
      //exit the function:
      return;
    }
  }
  //footer only:
  function addCopyright() {
    document.getElementById("copyrightYear").innerHTML = new Date().getFullYear();
  };
  addCopyright();
};
includeHTML();

function iconToggle(icon, section) {
  var isExpanded = $(event.target).attr("aria-expanded");
  if ($(section).attr("class") == "collapsing") {
    setInterval(50); //prevents double-clicking
    iconToggle();
  } else if (isExpanded == "true") {
    $(icon).addClass("icon-plus").removeClass("icon-minus");
  } else {
    $(icon).addClass("icon-minus").removeClass("icon-plus");
  }
};
